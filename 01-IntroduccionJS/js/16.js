// * Funciones. Arrow Functions

const sumar = (numero1, numero2) => {
   console.log(numero1 + numero2);
};

sumar(20, 5);

// * Cuando lo que se ejecuta sólo ocupa una línea se puede simplificar así:
const restar = (numero1, numero2) => console.log(numero1 - numero2);

console.warn("Separador");

restar(10, 50);
